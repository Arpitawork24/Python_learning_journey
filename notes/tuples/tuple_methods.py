# count element - count how many times element is repeated
tuple1 = (5, 1, 3, 2, 4, 2, 3, 1, 3, 2, 3)
res = tuple1.count(3) #3 occured 4 times
print('Count of 3 in tuple1 is:', res)
# index() is used to calculate the index value of the element when it 1st appeared
res2 = tuple1.index(2) #2 appeared at 4th index
print(res2)